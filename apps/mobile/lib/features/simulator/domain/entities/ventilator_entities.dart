// 2. PatientData — anthropometric data for IBW / protective Vt calculation
// ═══════════════════════════════════════════════════════════════════════════

/// Immutable patient demographic and anthropometric data.
///
/// Used to compute the Ideal Body Weight (IBW) and the protective tidal
/// volume range (6–8 mL/kg IBW) per the ARDSNet protocol.
@immutable
class PatientData {
  const PatientData({
    this.sex = Sex.male,
    this.heightCm = 170,
    this.weightKg = 70,
    this.age = 50,
  });

  /// Factory with sensible defaults for a 170 cm male, 70 kg, 50 y/o.
  factory PatientData.initial() => const PatientData();

  /// Biological sex — affects the IBW baseline constant.
  final Sex sex;

  /// Height in centimeters. Range: 140–210.
  final int heightCm;

  /// Actual body weight in kilograms. Range: 30–200.
  final int weightKg;

  /// Age in years. Range: 18–100.
  final int age;

  // ── Derived values ─────────────────────────────────────────────────────

  /// Ideal Body Weight (kg) using the ARDSNet / Devine formula.
  ///
  /// - Male:   IBW = 50.0 + 0.91 × (height_cm − 152.4)
  /// - Female: IBW = 45.5 + 0.91 × (height_cm − 152.4)
  double get ibw => sex == Sex.male
      ? 50.0 + 0.91 * (heightCm - 152.4)
      : 45.5 + 0.91 * (heightCm - 152.4);

  /// Body Mass Index (kg/m²) = weight / height².
  double get bmi => weightKg / math.pow(heightCm / 100.0, 2);

  /// Lower bound of protective tidal volume: 6 mL/kg IBW (mL).
  int get idealVt6 => (ibw * 6).round();

  /// Upper bound of protective tidal volume: 8 mL/kg IBW (mL).
  int get idealVt8 => (ibw * 8).round();

  PatientData copyWith({
    Sex? sex,
    int? heightCm,
    int? weightKg,
    int? age,
  }) =>
      PatientData(
        sex: sex ?? this.sex,
        heightCm: heightCm ?? this.heightCm,
        weightKg: weightKg ?? this.weightKg,
        age: age ?? this.age,
      );
}

