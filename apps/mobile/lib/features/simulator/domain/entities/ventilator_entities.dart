// 9. SimulationState — snapshot of the running simulation for the UI
// ═══════════════════════════════════════════════════════════════════════════

/// Immutable snapshot of the simulation state consumed by the UI layer.
///
/// Contains the rolling waveform buffers (pressure, flow, volume) ready
/// for rendering, the current write position, the active breath phase,
/// and the latest per-breath clinical metrics.
@immutable
class SimulationState {
  const SimulationState({
    required this.pressureData,
    required this.flowData,
    required this.volumeData,
    required this.currentIndex,
    required this.phase,
    required this.metrics,
  });

  /// Factory for the initial (blank-screen) state.
  ///
  /// Fills pressure with [peep] (baseline), flow and volume with 0.
  /// This ensures the waveform painters always have a full buffer to
  /// draw (avoids a blank canvas flash on reset).
  factory SimulationState.initial({double peep = 5.0}) {
    final bufSize = EngineConstants.bufferSize;
    return SimulationState(
      pressureData: List<double>.filled(bufSize, peep),
      flowData: List<double>.filled(bufSize, 0),
      volumeData: List<double>.filled(bufSize, 0),
      currentIndex: 0,
      phase: BreathPhase.expiration,
      metrics: const CycleMetrics(pip: 0, peep: 0, vte: 0, rr: 0),
    );
  }

  /// Rolling pressure waveform (cmH₂O).
  ///
  /// Length is up to [EngineConstants.bufferSize] samples. Earlier
  /// positions may be zero-padded before the first full buffer fill.
  final List<double> pressureData;

  /// Rolling flow waveform (L/min).
  final List<double> flowData;

  /// Rolling volume waveform (mL above FRC).
  final List<double> volumeData;

  /// Index of the most recent sample within the data lists.
  final int currentIndex;

  /// Current phase of the respiratory cycle (inspiration / expiration).
  final BreathPhase phase;

  /// Latest per-breath clinical metrics (PIP, PEEP, Vte, RR).
  ///
  /// Updated once per breath cycle when expiration begins. Before the
  /// first cycle completes, values are zero.
  final CycleMetrics metrics;

  SimulationState copyWith({
    List<double>? pressureData,
    List<double>? flowData,
    List<double>? volumeData,
    int? currentIndex,
    BreathPhase? phase,
    CycleMetrics? metrics,
  }) =>
      SimulationState(
        pressureData: pressureData ?? this.pressureData,
        flowData: flowData ?? this.flowData,
        volumeData: volumeData ?? this.volumeData,
        currentIndex: currentIndex ?? this.currentIndex,
        phase: phase ?? this.phase,
        metrics: metrics ?? this.metrics,
      );
}
