// 6. AbgFinding — single interpretive finding from ABG analysis
// ═══════════════════════════════════════════════════════════════════════════

/// A single clinical finding derived from ABG interpretation.
///
/// Examples:
/// - (warning, "Acidose respiratória: PaCO₂ 55 mmHg")
/// - (danger, "Hipoxemia grave: PaO₂/FiO₂ = 85")
@immutable
class AbgFinding {
  const AbgFinding({
    required this.level,
    required this.text,
  });

  /// Severity of this finding (ok, info, warning, danger).
  final AlertLevel level;

  /// Human-readable description in Portuguese.
  final String text;

  AbgFinding copyWith({
    AlertLevel? level,
    String? text,
  }) =>
      AbgFinding(
        level: level ?? this.level,
        text: text ?? this.text,
      );
}

// ═══════════════════════════════════════════════════════════════════════════
// 7. AbgAction — suggested ventilator adjustment
// ═══════════════════════════════════════════════════════════════════════════

/// A specific ventilator adjustment recommended based on ABG analysis.
///
/// Actions are ordered by [priority] (0 = urgent, 1 = important,
/// 2 = optimisation) and include enough context for the learner to
/// understand *what* to change, *why*, and *where* on the ventilator.
@immutable
class AbgAction {
  const AbgAction({
    required this.priority,
    required this.icon,
    required this.param,
    required this.action,
    required this.reason,
    required this.where,
  });

  /// Urgency tier: 0 = life-threatening, 1 = clinically important,
  /// 2 = fine-tuning / optimisation.
  final int priority;

  /// Emoji or icon identifier for visual cue (e.g. "🔺", "⬇️", "⚠️").
  final String icon;

  /// The ventilator parameter to adjust (e.g. "RR", "PEEP", "FiO₂").
  final String param;

  /// What to do (e.g. "Aumentar de 14 para 18 rpm").
  final String action;

  /// Clinical rationale (e.g. "PaCO₂ elevada → aumentar ventilação
  /// alveolar para corrigir acidose respiratória").
  final String reason;

  /// Physical location on the ventilator interface where the
  /// adjustment is made (e.g. "Painel principal → RR").
  final String where;

  AbgAction copyWith({
    int? priority,
    String? icon,
    String? param,
    String? action,
    String? reason,
    String? where,
  }) =>
      AbgAction(
        priority: priority ?? this.priority,
        icon: icon ?? this.icon,
        param: param ?? this.param,
        action: action ?? this.action,
        reason: reason ?? this.reason,
        where: where ?? this.where,
      );
}

// ═══════════════════════════════════════════════════════════════════════════
// 8. AbgAnalysis — complete ABG interpretation result
// ═══════════════════════════════════════════════════════════════════════════

/// Complete output of the ABG analysis engine.
///
/// Contains the primary acid-base disorder, a list of clinical findings
/// (with severity), actionable ventilator adjustments, and key derived
/// metrics (P/F ratio, driving pressure, Vt/kg, etc.).
@immutable
class AbgAnalysis {
  const AbgAnalysis({
    required this.primaryDisorder,
    required this.findings,
    required this.actions,
    required this.pfRatio,
    required this.drivingPressure,
    required this.pplat,
    required this.vtPerKg,
    required this.minuteVolume,
    required this.alveolarVentilation,
  });

  /// Factory for an empty / pre-analysis state.
  factory AbgAnalysis.initial() => const AbgAnalysis(
        primaryDisorder: '',
        findings: [],
        actions: [],
        pfRatio: 0,
        drivingPressure: 0,
        pplat: 0,
        vtPerKg: 0,
        minuteVolume: 0,
        alveolarVentilation: 0,
      );

  /// Primary acid-base disorder label.
  ///
  /// Examples: "Acidose respiratória aguda", "Alcalose metabólica
  /// com compensação respiratória", "Distúrbio misto".
  final String primaryDisorder;

  /// Ordered list of interpretive findings (most severe first).
  final List<AbgFinding> findings;

  /// Prioritised list of suggested ventilator adjustments.
  final List<AbgAction> actions;

  /// PaO₂ / FiO₂ ratio — key metric for ARDS classification.
  ///
  /// - > 300: normal oxygenation
  /// - 200–300: mild ARDS
  /// - 100–200: moderate ARDS
  /// - < 100: severe ARDS
  final double pfRatio;

  /// Driving pressure = Pplateau − PEEP (cmH₂O).
  ///
  /// Strong predictor of mortality in ARDS. Target ≤ 15 cmH₂O.
  final double drivingPressure;

  /// Plateau pressure (cmH₂O) — reflects alveolar distending pressure.
  ///
  /// Target ≤ 30 cmH₂O to prevent ventilator-induced lung injury.
  final double pplat;

  /// Tidal volume normalised to predicted body weight (mL/kg PBW).
  ///
  /// Lung-protective range: 6–8 mL/kg PBW.
  final double vtPerKg;

  /// Minute ventilation (L/min) = Vt × RR / 1000.
  final double minuteVolume;

  /// Alveolar ventilation (L/min) = (Vt − deadSpace) × RR / 1000.
  ///
  /// Estimated dead space ≈ 2 mL/kg IBW. This is the effective
  /// ventilation that participates in gas exchange.
  final double alveolarVentilation;

  AbgAnalysis copyWith({
    String? primaryDisorder,
    List<AbgFinding>? findings,
    List<AbgAction>? actions,
    double? pfRatio,
    double? drivingPressure,
    double? pplat,
    double? vtPerKg,
    double? minuteVolume,
    double? alveolarVentilation,
  }) =>
      AbgAnalysis(
        primaryDisorder: primaryDisorder ?? this.primaryDisorder,
        findings: findings ?? this.findings,
        actions: actions ?? this.actions,
        pfRatio: pfRatio ?? this.pfRatio,
        drivingPressure: drivingPressure ?? this.drivingPressure,
        pplat: pplat ?? this.pplat,
        vtPerKg: vtPerKg ?? this.vtPerKg,
        minuteVolume: minuteVolume ?? this.minuteVolume,
        alveolarVentilation:
            alveolarVentilation ?? this.alveolarVentilation,
      );
}

