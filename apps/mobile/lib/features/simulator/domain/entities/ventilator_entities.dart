// ═══════════════════════════════════════════════════════════════════════════
// 1. VentParams — all operator-settable ventilator parameters
// ═══════════════════════════════════════════════════════════════════════════

/// Immutable snapshot of ventilator knob settings and patient lung mechanics.
///
/// The ventilator mode determines which fields are actively used:
/// - **VCV**: [vt] and flow (derived from [vt] / [inspTime])
/// - **PCV**: [pip] (driving pressure above PEEP)
/// - **PSV**: [ps] (pressure support above PEEP) + [patientEffort]
@immutable
class VentParams {
  const VentParams({
    this.mode = VentMode.vcv,
    this.compliance = 50.0,
    this.resistance = 8.0,
    this.peep = 5.0,
    this.rr = 14,
    this.vt = 450,
    this.pip = 20.0,
    this.ps = 10.0,
    this.fio2 = 40,
    this.ieRatio = 2.0,
    this.patientEffort = 0.0,
    this.pHigh = 28.0,
    this.pLow = 0.0,
    this.tHigh = 4.5,
    this.tLow = 0.6,
    this.spontaneousRR = 0,
  });

  /// Factory with sensible defaults for a normal adult lung.
  factory VentParams.initial() => const VentParams();

  // ── Ventilator settings ────────────────────────────────────────────────

  /// Current ventilation mode (VCV, PCV, or PSV).
  final VentMode mode;

  /// Static lung compliance (mL/cmH₂O). Range: 10–100.
  ///
  /// Represents the lung's distensibility — how much volume is gained per
  /// unit of pressure. Low in ARDS/fibrosis (15–25), high in emphysema
  /// (80–100), normal ≈ 50.
  final double compliance;

  /// Total airway resistance (cmH₂O/L/s). Range: 3–40.
  ///
  /// Represents opposition to airflow through the airways. Normal ≈ 5–8.
  /// Elevated in bronchospasm (asthma 15–25) and COPD (15–20).
  /// Determines the resistive pressure component: P_res = Flow × R.
  final double resistance;

  /// Positive End-Expiratory Pressure (cmH₂O). Range: 0–25.
  ///
  /// Maintains alveolar recruitment at end-expiration, preventing
  /// atelectasis. Higher PEEP used in ARDS (10–18) to improve
  /// oxygenation; excessive PEEP risks hemodynamic compromise.
  final double peep;

  /// Respiratory rate (breaths per minute). Range: 6–40.
  ///
  /// Machine-set in VCV/PCV; patient-driven in PSV. Higher RR increases
  /// minute ventilation but reduces expiratory time, risking air trapping
  /// in obstructive disease.
  final int rr;

  /// Tidal volume target in mL (VCV mode). Range: 200–800.
  ///
  /// Lung-protective ventilation targets 6–8 mL/kg of predicted body
  /// weight. Exceeding 8 mL/kg PBW increases risk of ventilator-induced
  /// lung injury (VILI).
  final int vt;

  /// Peak Inspiratory Pressure above PEEP in cmH₂O (PCV mode). Range: 10–45.
  ///
  /// In PCV the ventilator delivers a square pressure wave; the achieved
  /// tidal volume depends on compliance: Vt ≈ C × (PIP − PEEP).
  final double pip;

  /// Pressure Support above PEEP in cmH₂O (PSV mode). Range: 5–30.
  ///
  /// Augments the patient's spontaneous effort. Weaning trials typically
  /// use PS 5–8 cmH₂O. Higher values compensate for weak respiratory
  /// muscles or high airway resistance.
  final double ps;

  /// Fraction of Inspired Oxygen (%). Range: 21–100.
  ///
  /// Room air = 21%. Target SpO₂ 92–96% in most patients. Prolonged
  /// FiO₂ > 60% risks oxygen toxicity. P/F ratio = PaO₂ / (FiO₂/100).
  final int fio2;

  /// Inspiratory-to-Expiratory ratio, expressed as 1:X. Range: 1.0–5.0.
  ///
  /// Normal 1:2. Obstructive disease benefits from longer expiration
  /// (1:3–1:4) to allow complete emptying. Inverse ratio (< 1:1) is
  /// rarely used outside specialised ARDS protocols.
  final double ieRatio;

  /// Patient's own inspiratory muscle effort in cmH₂O (PSV). Range: 0–10.
  ///
  /// Represents the negative pleural pressure generated by the diaphragm.
  /// Zero = fully passive; 5–10 = strong effort (e.g. during weaning).
  /// The engine subtracts this from alveolar pressure to drive flow.
  final double patientEffort;

  // ── APRV-specific parameters ──────────────────────────────────────────

  /// High pressure level in APRV mode (cmH₂O). Range: 15–40.
  /// Maintained for most of the cycle (T-high phase).
  final double pHigh;

  /// Low pressure level in APRV mode (cmH₂O). Range: 0–10.
  /// Brief release phase for CO₂ clearance (T-low phase).
  final double pLow;

  /// Duration of the high-pressure phase in APRV (seconds). Range: 2–8.
  final double tHigh;

  /// Duration of the release (low-pressure) phase in APRV (seconds).
  /// Range: 0.2–1.5. Typically 0.5–0.8 s.
  final double tLow;

  /// Spontaneous respiratory rate overlay in APRV (breaths/min).
  /// Range: 0–30. Represents small tidal breaths during P-high phase.
  final int spontaneousRR;

  // ── Derived values ─────────────────────────────────────────────────────

  /// RC time constant τ = R × C (seconds).
  ///
  /// Determines how quickly the lung fills/empties. After 3τ ≈ 95% of
  /// equilibrium volume is reached; after 5τ ≈ 99%. A long τ (high R
  /// and/or high C) requires longer expiratory time to avoid air trapping.
  double get tau => resistance * compliance / 1000.0;

  /// Total respiratory cycle time (seconds).
  /// APRV: T-high + T-low. Others: 60 / RR.
  double get totalCycleTime =>
      mode == VentMode.aprv ? tHigh + tLow : 60.0 / rr;

  /// Inspiratory time (seconds).
  /// APRV: T-high phase. Others: Ttotal / (1 + I:E).
  double get inspTime =>
      mode == VentMode.aprv ? tHigh : totalCycleTime / (1.0 + ieRatio);

  /// Expiratory time (seconds).
  /// APRV: T-low (release) phase. Others: Ttotal − Ti.
  double get expTime =>
      mode == VentMode.aprv ? tLow : totalCycleTime - inspTime;

  VentParams copyWith({
    VentMode? mode,
    double? compliance,
    double? resistance,
    double? peep,
    int? rr,
    int? vt,
    double? pip,
    double? ps,
    int? fio2,
    double? ieRatio,
    double? patientEffort,
    double? pHigh,
    double? pLow,
    double? tHigh,
    double? tLow,
    int? spontaneousRR,
  }) =>
      VentParams(
        mode: mode ?? this.mode,
        compliance: compliance ?? this.compliance,
        resistance: resistance ?? this.resistance,
        peep: peep ?? this.peep,
        rr: rr ?? this.rr,
        vt: vt ?? this.vt,
        pip: pip ?? this.pip,
        ps: ps ?? this.ps,
        fio2: fio2 ?? this.fio2,
        ieRatio: ieRatio ?? this.ieRatio,
        patientEffort: patientEffort ?? this.patientEffort,
        pHigh: pHigh ?? this.pHigh,
        pLow: pLow ?? this.pLow,
        tHigh: tHigh ?? this.tHigh,
        tLow: tLow ?? this.tLow,
        spontaneousRR: spontaneousRR ?? this.spontaneousRR,
      );
}

