// 3. BreathSample — single time-domain data point
// ═══════════════════════════════════════════════════════════════════════════

/// A single instantaneous sample from the ventilator simulation.
///
/// The engine produces one of these every [EngineConstants.dt] seconds.
/// These are collected into the rolling waveform buffer and rendered
/// as pressure, flow, and volume curves on the UI.
@immutable
class BreathSample {
  const BreathSample({
    required this.pressure,
    required this.flow,
    required this.volume,
    required this.phase,
  });

  /// Factory for a zeroed sample at the start of simulation.
  factory BreathSample.initial() => const BreathSample(
        pressure: 0,
        flow: 0,
        volume: 0,
        phase: BreathPhase.expiration,
      );

  /// Airway pressure at this instant (cmH₂O).
  ///
  /// In VCV this rises during inspiration due to elastic + resistive
  /// components. In PCV it is held constant (square wave).
  final double pressure;

  /// Instantaneous gas flow (L/min).
  ///
  /// Positive = inspiratory (into the lungs).
  /// Negative = expiratory (out of the lungs).
  final double flow;

  /// Instantaneous lung volume above functional residual capacity (mL).
  ///
  /// Starts at 0 (FRC) at the beginning of inspiration and returns
  /// toward 0 during expiration. If incomplete emptying occurs,
  /// the residual volume represents trapped gas (auto-PEEP).
  final double volume;

  /// Current phase of the respiratory cycle at this sample.
  final BreathPhase phase;

  BreathSample copyWith({
    double? pressure,
    double? flow,
    double? volume,
    BreathPhase? phase,
  }) =>
      BreathSample(
        pressure: pressure ?? this.pressure,
        flow: flow ?? this.flow,
        volume: volume ?? this.volume,
        phase: phase ?? this.phase,
      );
}

// ═══════════════════════════════════════════════════════════════════════════
// 4. CycleMetrics — per-breath derived clinical measurements
// ═══════════════════════════════════════════════════════════════════════════

/// Summary metrics for a single completed breath cycle.
///
/// These values are "frozen" once per cycle (at the start of expiration)
/// and displayed on the monitor panel. They mirror what a real ICU
/// ventilator screen shows.
@immutable
class CycleMetrics {
  const CycleMetrics({
    required this.pip,
    required this.peep,
    required this.vte,
    required this.rr,
  });

  /// Factory with zero values for pre-simulation state.
  factory CycleMetrics.initial() => const CycleMetrics(
        pip: 0,
        peep: 0,
        vte: 0,
        rr: 0,
      );

  /// Peak Inspiratory Pressure (cmH₂O) — the highest pressure reached
  /// during the inspiratory phase. Alarm threshold typically 40 cmH₂O.
  final int pip;

  /// End-Expiratory Pressure (cmH₂O) — measured PEEP including any
  /// auto-PEEP component. Should approximate the set PEEP in the
  /// absence of air trapping.
  final int peep;

  /// Expired Tidal Volume (mL) — the volume actually exhaled by the
  /// patient. In VCV this should match the set Vt unless there is a
  /// leak or the pressure limit is reached.
  final int vte;

  /// Measured Respiratory Rate (breaths/min) — derived from the time
  /// between consecutive breath onsets. May differ from set RR in PSV
  /// where the patient controls the rate.
  final int rr;

  /// Minute Ventilation (L/min) = Vte × RR / 1000.
  ///
  /// Normal range 5–8 L/min. Primary determinant of CO₂ elimination.
  double get minuteVolume => (vte * rr) / 1000.0;

  CycleMetrics copyWith({
    int? pip,
    int? peep,
    int? vte,
    int? rr,
  }) =>
      CycleMetrics(
        pip: pip ?? this.pip,
        peep: peep ?? this.peep,
        vte: vte ?? this.vte,
        rr: rr ?? this.rr,
      );
}

