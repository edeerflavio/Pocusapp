// 5. AbgInput — arterial blood gas input values
// ═══════════════════════════════════════════════════════════════════════════

/// Arterial blood gas (ABG) laboratory values entered by the learner.
///
/// These are the raw numbers from a blood gas analyser. The engine uses
/// them to determine acid-base status, oxygenation adequacy (P/F ratio),
/// and to suggest ventilator adjustments.
@immutable
class AbgInput {
  const AbgInput({
    this.ph = 7.40,
    this.pco2 = 40.0,
    this.hco3 = 24.0,
    this.pao2 = 90.0,
    this.sao2 = 97.0,
    this.lactato = 1.0,
  });

  /// Factory with textbook-normal ABG values.
  factory AbgInput.initial() => const AbgInput();

  /// Arterial pH. Normal: 7.35–7.45. Range: 6.80–7.80.
  ///
  /// < 7.35 = acidemia; > 7.45 = alkalemia. Extreme values (< 7.10 or
  /// > 7.60) are life-threatening.
  final double ph;

  /// Partial pressure of CO₂ in arterial blood (mmHg). Normal: 35–45.
  /// Range: 10–120.
  ///
  /// Regulated by alveolar ventilation. High PaCO₂ (hypercapnia) →
  /// respiratory acidosis; low PaCO₂ (hypocapnia) → respiratory alkalosis.
  final double pco2;

  /// Serum bicarbonate (mEq/L). Normal: 22–26. Range: 5–50.
  ///
  /// Metabolic component of acid-base. Low = metabolic acidosis
  /// (e.g. lactic acidosis, renal failure); high = metabolic alkalosis
  /// (e.g. vomiting, diuretic use).
  final double hco3;

  /// Partial pressure of O₂ in arterial blood (mmHg). Normal: 80–100.
  /// Range: 20–600.
  ///
  /// Primary measure of oxygenation. PaO₂ / FiO₂ < 300 defines ARDS.
  /// Values > 100 are normal on supplemental O₂.
  final double pao2;

  /// Arterial oxygen saturation (%). Normal: 95–100. Range: 50–100.
  ///
  /// Follows the oxyhemoglobin dissociation curve. SpO₂ < 90%
  /// corresponds to PaO₂ ≈ 60 mmHg (critical hypoxemia).
  final double sao2;

  /// Arterial lactate (mmol/L). Normal: < 2.0. Range: 0–30.
  ///
  /// Marker of tissue hypoperfusion / anaerobic metabolism.
  /// > 2 = elevated; > 4 = severe (associated with higher mortality
  /// in sepsis and shock).
  final double lactato;

  AbgInput copyWith({
    double? ph,
    double? pco2,
    double? hco3,
    double? pao2,
    double? sao2,
    double? lactato,
  }) =>
      AbgInput(
        ph: ph ?? this.ph,
        pco2: pco2 ?? this.pco2,
        hco3: hco3 ?? this.hco3,
        pao2: pao2 ?? this.pao2,
        sao2: sao2 ?? this.sao2,
        lactato: lactato ?? this.lactato,
      );
}

